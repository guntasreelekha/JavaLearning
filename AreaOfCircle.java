package com.concent;

//Compute the area of a circle
public class AreaOfCircle {
    public static void main(String args[]) {
        double pi, r, a;
        //radius of circle
        r = 14.4;
        //pi,approximately
        pi = 3.1416;
        //compute area
        a = pi * r * r;
        System.out.println("Area of circle is " + a);
    }
}
