package com.concent;

public class IfFor {
    public static void main(String args[]) {
        int a, b, i;
        for (i = 0; i <= 5; i++) {
            a = 20;
            b = 30;
            if (a < b) {
                System.out.println("a is greater than b");
            } else {
                System.out.println("b is greater than a");
            }
        }

    }
}
