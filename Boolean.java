package com.concent;

public class Boolean {
    public static void main(String args[]) {
        int a = 15, b = 34;
        System.out.println("a > b is " + (a > b));
        System.out.println("a < b is " + (a < b));
        System.out.println("a >= b is " + (a >= b));
        System.out.println("a <= b is " + (a <= b));
        System.out.println("a == b is " + (a == b));
        System.out.println("a != b is " + (a != b));
    }
}
